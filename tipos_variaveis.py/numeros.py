# Inteiro
numero_inteiro = 3
print("Inteiro = ", numero_inteiro)

# Real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante = ", numero_real)

# type()
print("Tipo da variavel inteiro = ", type(numero_inteiro))
print("Tipo de variavel real = ", type(numero_real))

# Soma +
soma = 1 + 1
print("1 + 1 = ", soma)

# Subtração -
subtracao = 1 - 1
print("1 - 1 = ", subtracao)

# Multiplicação *
multiplicacao = 2 * 2
print("2 x 2 = ", multiplicacao)

# Divisão /
divisao = 5 / 2
print("5 / 2 = ", divisao)
print("Tipos de variavel do resultado da divisão = ", type(divisao))

# int()
print("Valor em inteiro = ", int(divisao))

# float()
print("Valor em float = ", float(soma))

# Modulo %
modulo = 5 % 2
print("Modulo = ", modulo)

# Divisão Inteiro //
divisao = 5 // 2
print("5 // 2 = ", divisao)
print("Tipos de variavel do resultado da divisão inteiro = ", type(divisao))